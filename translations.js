function setLanguage(lang) {
    if (!languageData[lang]) return;

    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (languageData[lang][key]) {
            element.innerHTML = languageData[lang][key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (placeholders[lang] && placeholders[lang][key]) {
            element.setAttribute('placeholder', placeholders[lang][key]);
        }
    });

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });

    localStorage.setItem('userLang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    let savedLang = localStorage.getItem('userLang');
    if (!savedLang) {
        const browserLang = navigator.language || navigator.userLanguage; 
        savedLang = browserLang.startsWith('de') ? 'de' : browserLang.startsWith('it') ? 'it' : 'en';
    }
    setLanguage(savedLang);
});
